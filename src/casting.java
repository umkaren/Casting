public class casting {
    public static void main(String[] args) {
        double data = 112.35;
        System.out.println("Double: " + data);

        int num = (int)data;
        System.out.println("int: " + num);

        String words = "49";
        System.out.println("The string value is: " + words);

        int numbs = Integer.parseInt(words);
        System.out.println("The integer is: " + numbs);

    }
}
